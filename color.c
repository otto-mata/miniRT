#include "miniRT.h"

uint64_t	color(uint8_t a, uint8_t r, uint8_t g, uint8_t b)
{
	uint64_t	color;

	color = ((a & 0xff) << 24) + ((r & 0xff) << 16) + ((g & 0xff) << 8)
		+ (b & 0xff);
	return (color);
}
